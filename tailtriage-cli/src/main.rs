use std::path::PathBuf;

use clap::{Parser, ValueEnum};
use serde_json::Value;
use tailtriage_analyzer::{render_json_pretty, render_text, try_analyze_run};
use tailtriage_cli::artifact::load_run_artifact;
use tailtriage_cli::{analyzer_options_help_text, build_analyze_options};
use tailtriage_tracing::{import_jsonl_path, ImportOptions};

#[derive(Debug, Parser)]
#[command(name = "tailtriage")]
#[command(about = "Analyze tailtriage run artifacts", version)]
struct Cli {
    #[command(subcommand)]
    command: Command,
}

#[derive(Debug, clap::Subcommand)]
enum Command {
    /// Import tracing JSONL spans into a tailtriage run JSON artifact.
    Import {
        #[command(subcommand)]
        command: ImportCommand,
    },
    /// Analyze one tailtriage run JSON artifact.
    Analyze {
        /// Path to a run JSON file generated by tailtriage-core.
        #[arg(value_name = "RUN_JSON")]
        run_json: Option<PathBuf>,
        /// Output format for the diagnosis report.
        #[arg(long, value_enum, default_value_t = OutputFormat::Text)]
        format: OutputFormat,
        /// Path to analyzer TOML config. Applied before `--analyzer-set` overrides.
        #[arg(long, value_name = "TOML")]
        analyzer_config: Option<PathBuf>,
        /// Analyzer override in `path=value` form; may be repeated.
        #[arg(long = "analyzer-set", value_name = "PATH=VALUE")]
        analyzer_set: Vec<String>,
        /// Print analyzer option help and exit successfully.
        #[arg(long)]
        help_analyzer_options: bool,
    },
}

#[derive(Debug, clap::Subcommand)]
enum ImportCommand {
    /// Import completed tracing span records from JSONL into run JSON.
    TracingJson {
        /// Path to newline-delimited JSON span records.
        #[arg(value_name = "SPANS_JSONL")]
        spans_jsonl: PathBuf,
        /// Service name to store in imported run metadata.
        #[arg(long, value_name = "SERVICE")]
        service: String,
        /// Path where imported run JSON should be written.
        #[arg(long, value_name = "RUN_JSON")]
        output: PathBuf,
        /// Optional service version to store in run metadata.
        #[arg(long, value_name = "VERSION")]
        service_version: Option<String>,
        /// Optional explicit run id for imported run metadata.
        #[arg(long, value_name = "RUN_ID")]
        run_id: Option<String>,
        /// Fail on malformed/incomplete tailtriage spans.
        #[arg(long)]
        strict: bool,
        /// Declares expected input shape for tracing import.
        #[arg(long, value_enum, default_value_t = TracingInputFormat::Auto)]
        input_format: TracingInputFormat,
    },
}

#[derive(Debug, Clone, Copy, PartialEq, Eq, ValueEnum)]
enum OutputFormat {
    Text,
    Json,
}

#[derive(Debug, Clone, Copy, PartialEq, Eq, ValueEnum)]
enum TracingInputFormat {
    Auto,
    TailtriageSpanJsonl,
    TracingSubscriberFmtJson,
}

fn reject_unsupported_fmt_json(
    path: &std::path::Path,
    input_format: TracingInputFormat,
) -> Result<(), Box<dyn std::error::Error>> {
    if matches!(input_format, TracingInputFormat::TracingSubscriberFmtJson) {
        return Err(std::io::Error::new(std::io::ErrorKind::InvalidInput,
            "--input-format tracing-subscriber-fmt-json is not supported for import in this release. tailtriage needs completed span records with started_at_unix_ms and finished_at_unix_ms. Recommended setup: add tailtriage_tracing::TracingIntakeSession layer, write completed-span JSONL, then run: tailtriage import tracing-json <completed-spans.jsonl> --input-format tailtriage-span-jsonl --service <service> --output <run-json>"
        ).into());
    }
    let f = std::fs::File::open(path)?;
    let r = std::io::BufRead::lines(std::io::BufReader::new(f));
    for line in r.take(20) {
        let line = line?;
        let t = line.trim();
        if t.is_empty() {
            continue;
        }
        let v: Value = match serde_json::from_str(t) {
            Ok(v) => v,
            Err(_) => break,
        };
        let looks_fmt =
            v.get("fields").is_some() && v.get("span").is_none() && v.get("level").is_some();
        if looks_fmt {
            return Err(std::io::Error::new(std::io::ErrorKind::InvalidData,
                "input appears to be tracing_subscriber::fmt().json() event logs without completed span start/end timing. tailtriage import needs completed tt.* span records. Recommended: capture with tailtriage_tracing::TracingIntakeSession and import that completed-span JSONL."
            ).into());
        }
        break;
    }
    Ok(())
}
fn run() -> Result<(), Box<dyn std::error::Error>> {
    let cli = Cli::parse();

    match cli.command {
        Command::Import { command } => match command {
            ImportCommand::TracingJson {
                spans_jsonl,
                service,
                output,
                service_version,
                run_id,
                strict,
                input_format,
            } => {
                let mut options = ImportOptions::new(service).strict(strict);
                if let Some(service_version) = service_version {
                    options = options.service_version(service_version);
                }
                if let Some(run_id) = run_id {
                    options = options.run_id(run_id);
                }

                reject_unsupported_fmt_json(&spans_jsonl, input_format)?;
                let imported = import_jsonl_path(spans_jsonl, options)?;
                for warning in imported.warnings() {
                    eprintln!("warning: {}", warning.message());
                }
                if imported.run().requests.is_empty() {
                    return Err(std::io::Error::new(
                        std::io::ErrorKind::InvalidData,
                        "tracing import produced zero request events; tailtriage needs completed tt.* request spans with start/end timestamps. Use TracingIntakeSession layer capture, then import the completed-span JSONL.",
                    )
                    .into());
                }

                let file = std::fs::File::create(&output)?;
                serde_json::to_writer_pretty(file, imported.run())?;
            }
        },
        Command::Analyze {
            run_json,
            format,
            analyzer_config,
            analyzer_set,
            help_analyzer_options,
        } => {
            if help_analyzer_options {
                println!("{}", analyzer_options_help_text());
                return Ok(());
            }

            let run_json = run_json.ok_or_else(|| {
                clap::Error::raw(
                    clap::error::ErrorKind::MissingRequiredArgument,
                    "missing required RUN_JSON argument (or use --help-analyzer-options)",
                )
            })?;
            let loaded = load_run_artifact(&run_json)?;
            for warning in &loaded.warnings {
                eprintln!("warning: {warning}");
            }
            let options = build_analyze_options(analyzer_config.as_deref(), &analyzer_set)?;
            let report = try_analyze_run(&loaded.run, options)?;

            match format {
                OutputFormat::Text => {
                    println!("{}", render_text(&report));
                }
                OutputFormat::Json => {
                    println!("{}", render_json_pretty(&report)?);
                }
            }
        }
    }

    Ok(())
}

fn main() {
    if let Err(err) = run() {
        eprintln!("{err}");
        std::process::exit(1);
    }
}
